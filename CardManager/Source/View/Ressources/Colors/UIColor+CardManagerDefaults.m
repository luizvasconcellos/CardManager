#import "UIColor+CardManagerDefaults.h"

@implementation UIColor (CardManagerDefaults)

+ (UIColor *)greyishBrownColor {
    return [UIColor colorWithWhite:74.0f / 255.0f alpha:1.0f];
}

+ (UIColor *)whiteTwoColor {
    return [UIColor colorWithWhite:255.0f / 255.0f alpha:1.0f];
}

+ (UIColor *)orangishColor {
    return [UIColor colorWithRed:255.0f / 255.0f green:120.0f / 255.0f blue:50.0f / 255.0f alpha:1.0f];
}

@end
